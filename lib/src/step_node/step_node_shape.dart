/// An enumeration representing the different shapes that a step node can have.
///
/// The available shapes are:
/// - `circle`: A circular shape.
/// - `square`: A square shape.
/// - `rectangle`: A rectangular shape.
/// - `triangle`: A triangular shape.
/// - `diamond`: A diamond shape.
/// - `polygon`: A polygonal shape.
enum StepNodeShape {
  circle,
  square,
  rectangle,
  triangle,
  diamond,
  polygon,
}
